def getQuantityForItem(itemName, questElements):
    for questElement in questElements:
        if itemName in questElement.text:
            matches = re.findall(r"\(\d+\)", questElement.text)
            match = matches[-1]
            return match.removeprefix("(").removesuffix(")")


