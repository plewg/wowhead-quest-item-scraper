def getObjectiveOfDataLine(elements):
    objectiveOfFound = False
    for element in elements:
        lines = element.text.split("\n")
        for line in lines:
            if "objective-of" in line:
                objectiveOfFound = True
            if objectiveOfFound and "data:" in line:
                return line


